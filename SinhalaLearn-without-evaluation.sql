CREATE TYPE "chat_mode" AS ENUM (
  'learning',
  'evaluation'
);

CREATE TYPE "chat_channel" AS ENUM (
  'text',
  'voice',
  'mixed'
);

CREATE TYPE "message_role" AS ENUM (
  'user',
  'assistant',
  'system'
);

CREATE TYPE "message_modality" AS ENUM (
  'text',
  'voice'
);

CREATE TYPE "grade_level" AS ENUM (
  '6 - 8',
  '9 - 11',
  '12 - 13',
  'university'
);

CREATE TYPE "resource_source" AS ENUM (
  'user_upload',
  'url',
  'system'
);

CREATE TYPE "resource_role" AS ENUM (
  'syllabus',
  'question_paper',
  'answer_script'
);

CREATE TYPE "evaluation_status" AS ENUM (
  'pending',
  'processing',
  'completed',
  'failed'
);

CREATE TABLE "users" (
  "id" uuid PRIMARY KEY,
  "email" varchar UNIQUE NOT NULL,
  "full_name" varchar,
  "password_hash" varchar NOT NULL,
  "created_at" timestamptz,
  "updated_at" timestamptz
);

CREATE TABLE "chat_sessions" (
  "id" uuid PRIMARY KEY,
  "user_id" uuid NOT NULL,
  "mode" chat_mode NOT NULL DEFAULT 'learning',
  "channel" chat_channel NOT NULL DEFAULT 'text',
  "title" varchar,
  "description" text,
  "grade" int,
  "subject" varchar,
  "created_at" timestamptz,
  "updated_at" timestamptz
);

CREATE TABLE "resource_files" (
  "id" uuid PRIMARY KEY,
  "user_id" uuid NOT NULL,
  "original_filename" varchar,
  "storage_path" varchar,
  "mime_type" varchar,
  "size_bytes" bigint,
  "source_type" resource_source,
  "language" varchar,
  "created_at" timestamptz
);

CREATE TABLE "session_resources" (
  "session_id" uuid NOT NULL,
  "resource_id" uuid NOT NULL,
  "label" varchar,
  PRIMARY KEY ("session_id", "resource_id")
);

CREATE TABLE "messages" (
  "id" uuid PRIMARY KEY,
  "session_id" uuid NOT NULL,
  "role" message_role,
  "modality" message_modality,
  "content" text,
  "grade_level" grade_level,
  "audio_url" varchar,
  "transcript" text,
  "audio_duration_sec" numeric,
  "model_name" varchar,
  "prompt_tokens" int,
  "completion_tokens" int,
  "total_tokens" int,
  "created_at" timestamptz
);

CREATE TABLE "message_attachments" (
  "id" uuid PRIMARY KEY,
  "message_id" uuid NOT NULL,
  "resource_id" uuid NOT NULL,
  "display_name" varchar,
  "attachment_type" varchar,
  "created_at" timestamptz
);

CREATE TABLE "message_safety_reports" (
  "id" uuid PRIMARY KEY,
  "message_id" uuid NOT NULL,
  "missing_concepts" jsonb,
  "extra_concepts" jsonb,
  "flagged_sentences" jsonb,
  "reasoning" jsonb,
  "created_at" timestamptz
);

CREATE TABLE "resource_chunks" (
  "id" uuid PRIMARY KEY,
  "resource_id" uuid NOT NULL,
  "chunk_index" int,
  "content" text,
  "content_length" int,
  "token_count" int,
  "embedding" vector(1536),
  "embedding_model" varchar,
  "start_char" int,
  "end_char" int
);

CREATE TABLE "message_context_chunks" (
  "id" BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "message_id" uuid NOT NULL,
  "chunk_id" uuid NOT NULL,
  "similarity_score" numeric,
  "rank" int
);

CREATE TABLE "evaluation_sessions" (
  "id" uuid PRIMARY KEY,
  "session_id" uuid NOT NULL,
  "rubric_id" uuid,
  "status" evaluation_status,
  "created_at" timestamptz
);

CREATE TABLE "evaluation_resources" (
  "id" uuid PRIMARY KEY,
  "evaluation_session_id" uuid NOT NULL,
  "resource_id" uuid NOT NULL,
  "role" resource_role
);

CREATE TABLE "paper_config" (
  "id" uuid PRIMARY KEY,
  "evaluation_session_id" uuid NOT NULL,
  "total_marks" int,
  "total_main_questions" int,
  "required_questions" int,
  "created_at" timestamptz
);

CREATE TABLE "question_papers" (
  "id" uuid PRIMARY KEY,
  "evaluation_session_id" uuid NOT NULL,
  "resource_id" uuid NOT NULL,
  "extracted_text" text,
  "created_at" timestamptz
);

CREATE TABLE "questions" (
  "id" uuid PRIMARY KEY,
  "question_paper_id" uuid NOT NULL,
  "question_number" varchar,
  "question_text" text,
  "max_marks" int
);

CREATE TABLE "sub_questions" (
  "id" uuid PRIMARY KEY,
  "question_id" uuid NOT NULL,
  "label" varchar,
  "sub_question_text" text,
  "max_marks" int
);

CREATE TABLE "rubrics" (
  "id" uuid PRIMARY KEY,
  "name" varchar,
  "description" text,
  "rubric_type" varchar,
  "created_by" uuid,
  "created_at" timestamptz
);

CREATE TABLE "rubric_criteria" (
  "id" uuid PRIMARY KEY,
  "rubric_id" uuid NOT NULL,
  "criterion" varchar,
  "weight_percentage" int,
  "created_at" timestamptz
);

CREATE TABLE "answer_documents" (
  "id" uuid PRIMARY KEY,
  "evaluation_session_id" uuid NOT NULL,
  "resource_id" uuid NOT NULL,
  "student_identifier" varchar,
  "created_at" timestamptz
);

CREATE TABLE "evaluation_results" (
  "id" uuid PRIMARY KEY,
  "answer_document_id" uuid NOT NULL,
  "total_score" numeric,
  "overall_feedback" text,
  "evaluated_at" timestamptz
);

CREATE TABLE "question_scores" (
  "id" uuid PRIMARY KEY,
  "evaluation_result_id" uuid NOT NULL,
  "sub_question_id" uuid NOT NULL,
  "awarded_marks" numeric,
  "feedback" text
);

